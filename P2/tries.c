#include "tries.h"
#include<stdio.h>
#include<stdlib.h>

struct Node{
    char c;
    char isEnd;
    struct Node* left;
    struct Node* center;
    struct Node* right;
};

struct Node* head;
//========================helper functions================================

struct Node* puthelp(struct Node* curr, char* word, int index, int length) {
    printf("at index %d\n",index);
    if (index == length - 1) {
        // Reached the end of the word
        if (curr == NULL) {
            // Allocate memory for a new node
            curr = (struct Node*)malloc(sizeof(struct Node));
            curr->c = word[index];
            curr->isEnd = 1;
            curr->left = curr->center = curr->right = NULL;
        } else {
            // Mark this node as the end of a word
            curr->isEnd = 1;
        }
        return curr;
    }

    char c = word[index];
    if (curr == NULL) {
        // Allocate memory for a new node
        curr = (struct Node*)malloc(sizeof(struct Node));
        curr->c = c;
        curr->isEnd = 0;
        curr->left = curr->center = curr->right = NULL;
    }

    if (c < curr->c) {
        curr->left = puthelp(curr->left, word, index + 1, length);
    } else if (c > curr->c) {
        curr->right = puthelp(curr->right, word, index + 1, length);
    } else {
        curr->center = puthelp(curr->center, word, index + 1, length);
    }
    
    return curr;
}

void destroy_helper(struct Node* curr){
    if(curr == NULL) return;
    destroy_helper(curr->left);
    destroy_helper(curr->center);
    destroy_helper(curr->right);
    free(curr);
}
void traverseboi(struct Node* curr){
    if(curr == NULL) return;
    traverseboi(curr->left);
    traverseboi(curr->center);
    traverseboi(curr->right);
}
struct Node* get_helper(struct Node* curr, char* word, int index) {
    if (curr == NULL) {
        return NULL;
    }

    char c = word[index];
    printf("Find: %c",curr->c);
    if (c < curr->c) {
        return get_helper(curr->left, word, index);
    } else if (c > curr->c) {
        return get_helper(curr->right, word, index);
    } else {
        if (word[index + 1] == '\0') {
            // Reached the end of the word
            if (curr->isEnd) {
                return curr; // Found the word
            } else {
                return NULL; // Word not found
            }
        } else {
            // Continue searching in the center subtree
            return get_helper(curr->center, word, index + 1);
        }
    }
}

